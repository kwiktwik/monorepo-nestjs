/**
 * Subscription provider interface
 * 
 * All providers must implement this interface.
 * The implementation differs based on subscription type:
 * - PROVIDER_MANAGED: Provider handles auto-charging and retries
 * - USER_MANAGED: We initiate charges and handle retries
 */
export interface SubscriptionProvider {
  // === Identity ===
  /** Provider type */
  readonly provider: PaymentProvider;
  /** Subscription type this provider handles */
  readonly subscriptionType: SubscriptionType;

  // === Initialization ===
  /** Initialize provider with configuration */
  initialize(config: AnyProviderConfig): void;
  /** Get public configuration for client-side */
  getPublicConfig(): Record<string, unknown>;

  // === Subscription Lifecycle ===
  /** Setup a new subscription */
  setupSubscription(params: SetupSubscriptionParams): Promise<SetupSubscriptionResult>;
  /** Activate subscription (after mandate approval) */
  activateSubscription?(
    merchantSubscriptionId: string,
    providerSubscriptionId: string,
  ): Promise<SubscriptionStatusResult>;
  /** Cancel subscription */
  cancelSubscription(params: CancelSubscriptionParams): Promise<CancelSubscriptionResult>;
  /** Pause subscription */
  pauseSubscription?(
    merchantSubscriptionId: string,
    providerSubscriptionId: string,
  ): Promise<SubscriptionStatusResult>;
  /** Resume subscription */
  resumeSubscription?(
    merchantSubscriptionId: string,
    providerSubscriptionId: string,
  ): Promise<SubscriptionStatusResult>;
  /** Get subscription status */
  getSubscriptionStatus(params: GetSubscriptionStatusParams): Promise<SubscriptionStatusResult>;

  // === Charging ===
  /** Charge subscription (behavior differs by type) */
  chargeSubscription(params: ChargeSubscriptionParams): Promise<ChargeSubscriptionResult>;
  /** Notify about upcoming charge (PhonePe-specific) */
  notifyCharge?(params: ChargeSubscriptionParams): Promise<ChargeSubscriptionResult>;

  // === Order Management ===
  /** Get order status */
  getOrderStatus(params: GetOrderStatusParams): Promise<OrderStatusResult>;

  // === Refunds ===
  /** Refund a payment */
  refundPayment?(params: RefundPaymentParams): Promise<RefundPaymentResult>;

  // === Webhooks ===
  /** Parse webhook event */
  parseWebhookEvent(params: ParseWebhookParams): Promise<WebhookEvent>;
  /** Verify webhook signature */
  verifyWebhookSignature(
    payload: string | Record<string, unknown>,
    signature: string,
  ): boolean;

  // === Health Check ===
  /** Check provider health */
  healthCheck(): Promise<{ readonly healthy: boolean; readonly message: string | null }>;
}
