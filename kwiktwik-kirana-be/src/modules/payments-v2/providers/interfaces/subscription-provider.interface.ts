// ============================================================================
// Provider Configuration
// ============================================================================

/**
 * Base provider configuration
 */
export interface ProviderConfig {
  /** Unique configuration ID */
  readonly configId: string;
  /** Provider type */
  readonly provider: PaymentProvider;
  /** App ID this config belongs to */
  readonly appId: string;
  /** Environment */
  readonly environment: 'SANDBOX' | 'PRODUCTION';
  /** Whether this config is enabled */
  readonly enabled: boolean;
  /** Is this the default config for the app */
  readonly isDefault: boolean;
  /** Webhook secret for signature verification */
  readonly webhookSecret: string | null;
}

/**
 * Razorpay-specific configuration
 */
export interface RazorpayProviderConfig extends ProviderConfig {
  readonly provider: 'RAZORPAY';
  readonly keyId: string;
  readonly keySecret: string;
  readonly webhookSecret: string | null;
  readonly accountId: string | null;
}

/**
 * PhonePe-specific configuration
 */
export interface PhonePeProviderConfig extends ProviderConfig {
  readonly provider: 'PHONEPE';
  readonly clientId: string;
  readonly clientSecret: string;
  readonly clientVersion: number;
  readonly merchantId: string;
  readonly saltKey: string | null;
  readonly saltIndex: string | null;
  /** Which PhonePe integration flow to use. Defaults to API_INTEGRATION. */
  readonly checkoutMode?: 'STANDARD_CHECKOUT' | 'API_INTEGRATION';
}

/**
 * Union type for all provider configs
 */
export type AnyProviderConfig = RazorpayProviderConfig | PhonePeProviderConfig;

